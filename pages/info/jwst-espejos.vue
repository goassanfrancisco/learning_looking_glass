    <paragraph>El espejo primario de JWST es un reflector de berilio de 6,5 metros de diámetro, recubierto de oro, con un área de recolección de 25 m². Estas dimensiones son demasiado grandes para los vehículos de lanzamiento actuales, por lo que al espejo lo componen 18 segmentos hexagonales, que se desplegaron una vez que se abrio el telescopio.</paragraph>

    <heading tag="h3">¿Por qué hexagonal?</heading>

    <paragraph>La forma hexagonal permite obtener un espejo aproximadamente circular y segmentado con "un alto factor de llenado y una simetría séxtuple". Un alto factor de llenado significa que los segmentos encajan entre sí sin espacios. Si los segmentos fueran circulares, habría huecos entre ellos. La simetría es buena porque sólo tiene que haber 3 prescripciones ópticas diferentes para 18 segmentos, 6 de cada uno (véase el diagrama de arriba a la derecha).</paragraph>

    <paragraph>Por último, se desea una forma de espejo global aproximadamente circular porque eso enfoca la luz en la región más compacta de los detectores. Un espejo ovalado, por ejemplo, daría imágenes alargadas en una dirección. Un espejo cuadrado enviaría gran parte de la luz fuera de la región central.</paragraph>

    <heading tag="h3">¿Por qué berilio?</heading>

    <paragraph>El berilio es un metal ligero (símbolo atómico: Be) que tiene muchas características que lo hacen deseable para el espejo primario de Webb. En particular, el berilio es muy fuerte para su peso y es bueno para mantener su forma en un rango de temperaturas. El berilio es un buen conductor de la electricidad y el calor, y no es magnético.</paragraph>

    <paragraph>Por su ligereza y resistencia, el berilio se utiliza a menudo para construir piezas de aviones supersónicos (más rápidos que la velocidad del sonido) y del transbordador espacial. También se utiliza en aplicaciones más cotidianas, como muelles y herramientas. Hay que tener especial cuidado al trabajar con el berilio, ya que no es saludable respirar o ingerir polvo de berilio.</paragraph>

    <heading tag="h3">Capa de oro</heading>

